
public class Token {
	
	private String lexema;
	private CompLexico compLexico;
	
	public Token (){
		
	}

	public String getLexema() {
		return lexema;
	}

	public void setLexema(String lexema) {
		this.lexema = lexema;
	}

	public CompLexico getCompLexico() {
		return compLexico;
	}

	public void setCompLexico(CompLexico compLexico) {
		this.compLexico = compLexico;
	}
	
	

}
